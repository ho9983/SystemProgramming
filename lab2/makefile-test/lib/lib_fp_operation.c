int plus(int a, int b) {
	int result = 0;
	result = a + b;
	return result;
}

int minus(int a, int b) {
	int result = 0;
	result = a - b;
	return result;
}

int multi(int a, int b) {
	int result = 0;
	result = a * b;
	return result;
}

int divide(int a, int b) {
	int result = 0;
	result = a / b;
	return result;
}
